def suma(a, b):
    resultado= a+b
    return resultado

def resta (num1, num2):
    resultado= num1-num2
    return resultado

def multiplicacion(parametro1, parametro2):
    resultado= parametro1*parametro2
    return resultado

def division(a, b):
    resultado= a/b
    return resultado

num1=10
num2=20

print("suma de dos variables: " , suma(num1, num2))
print("resta de dos variables: " , resta(num1, num2))
print("multiplicacion de dos variables: " , multiplicacion(num1, num2))
print("division de dos variables: " , division(num1, num2))

